// while loop 
import java.lang.*;
import java.util.*;
class While{
    public static void main(String args[])
    {
        int i=1;
        while(i<100)
        {
            System.out.println(i);
            i=i*2;
        }
        int n=100;
        do {
            System.out.println(n);
            n=n*2;
             }
        while(n<100);  // dont forget ;
    }
}

